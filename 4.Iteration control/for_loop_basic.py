# Example of for loop using range() :-
# single arg range()
for i in range(5):
    print(i)
# Two arg range()
# print the table of 4:->
print("table of 4")
for i in range(1, 11):
    print(i*4)
print("All element of AP where a=1,d=3, l=30")
# 3 arg range()
for i in range(1, 30, 3):
    print(i)
print("10 to 1")
for i in range(10, 0, -1):
    print(i)