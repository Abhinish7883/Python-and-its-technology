# indexing in python.
a = "Abhinish tiwari"

print(a[14])
print(a[-1])

print(a[3])
print(a[-11])

# slicing in python.
# to print 0 to 7 index string.
print(a[0:8])
# To print alternative index string.
print(a[0::2])
print(a[9:15])
# slicing with negative index.
print(a[-1::-1]) # revers the string using slice concepts
# alternative index from right side.
print(a[-1::-2])
print(a[2::2])